    private void Tampilbuku(){
        DefaultTableModel tbl = new  DefaultTableModel();
        tbl.addColumn("Kode Buku");
        tbl.addColumn("Judul Buku");
        tbl.addColumn("Pengarang");
        tbl.addColumn("Penerbit");
        tbl.addColumn("Tahun Terbit");
        tbl.addColumn("Harga Buku");
        tabel1.setModel(tbl);
        try {
            st =  koneksi.createStatement();
            ResultSet res = st.executeQuery("Select * from buku");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("kodeBuku"),
                    res.getString("judulBuku"),
                    res.getString("pengarang"),
                    res.getString("penerbit"),
                    res.getString("tahunTerbit"),
                    res.getString("hargaBuku"),
                });
                tabel1.setModel(tbl);
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Gagal Tampil "+e);
        }
    }
    
