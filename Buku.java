    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        String kodebuku = txtkodebuku.getText();
        String judulbuku = txtjdlbuku.getText();
        String pengarang = txtpengarang.getText();
        String thntebit = txtthnterbit.getText();
        String penerbit = txtpenerbit.getText();
        String harga = txtharga.getText();
        try {
            st = koneksi.createStatement();
                st.executeUpdate("update buku SET kodeBuku='"+kodebuku+"',"
                        +"judulBuku='"+judulbuku+"',"
                        +"pengarang='"+pengarang+"',"
                        +"penerbit='"+penerbit+"',"
                        +"tahunTerbit='"+thntebit+"',"
                        +"hargabuku='"+harga+"'" 
                        + "WHERE kodeBuku='"+kodebuku+"' OR judulBuku='"+judulbuku+"'");
            Tampilbuku();
            JOptionPane.showMessageDialog(null, "Teredit");
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Tidak Teredit\n"+e.getMessage());
        }
    }//GEN-LAST:event_beditActionPerformed

