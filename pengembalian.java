    private void tampilkan() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No. Pengembalian");
        model.addColumn("Stambuk");
        model.addColumn("Tanggal Peminjaman");
        model.addColumn("Tanggal Pengembalian");
        model.addColumn("No. Peminjaman");
        model.addColumn("Lama Pinjam");
        model.addColumn("Denda");
        
        tabelkembali.setModel(model);
        try {
            st = koneksi.createStatement();
            Res = st.executeQuery("select * from pengembalian");
            while (Res.next()) {
                model.addRow(new Object[]{
                    Res.getString("noPengembalian"),
                    Res.getString("stambuk"),
                    Res.getDate("tanggalPinjam"),
                    Res.getDate("tanggalKembali"),
                    Res.getString("noPeminjaman"),
                    Res.getInt("keterlambatan"),
                    Res.getInt("denda"),
                });
                tabelkembali.setModel(model);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }
    
    private void pilihpinjam() {
        cbtnopin.removeAllItems();
        cbtnopin.addItem("Select");
        try {
            String sql = "select * from peminjaman";
            st = koneksi.createStatement();
            Res = st.executeQuery(sql);
            while (Res.next()) {
                String nopin = Res.getString("noPeminjaman");
                cbtnopin.addItem(nopin);
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

