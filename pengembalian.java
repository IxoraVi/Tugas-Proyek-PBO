    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String nokem = txtnokem.getText();
        SimpleDateFormat format = new SimpleDateFormat ("yyyy-MM-dd");
            tanggal = String.valueOf(format.format(jpinjam.getDate()));
            tanggal1 = String.valueOf(format.format(jkembali.getDate()));
        String nopin = cbtnopin.getItemAt(cbtnopin.getSelectedIndex()).toString();
        String stambuk = txtstambuk.getText();
        String lambat = txtlambat.getText();
        String denda = jmldenda.getText();

        try {
            st = koneksi.createStatement();
            st.executeUpdate("insert into pengembalian VALUES ('"+ nokem +"','"
                    + stambuk +"','"
                    + tanggal +"','"
                    + tanggal1 +"','"
                    + nopin +"','"
                    + lambat +"','"
                    + denda +"');");
            tampilkan();
            JOptionPane.showMessageDialog(null, "Data Behasil Disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+ e.getMessage());
        }
    }//GEN-LAST:event_bsimpanActionPerformed

