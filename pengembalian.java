    private void tabelkembaliMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelkembaliMouseClicked
        int a = tabelkembali.getSelectedRow();
        if (a == -1){
            return;
        }
        String nokem = tabelkembali.getValueAt(a, 0).toString();
        txtnokem.setText(nokem);
        
        String nopin = tabelkembali.getValueAt(a, 1).toString();
        cbtnopin.setSelectedItem(nopin); 
        
        String kembali = tabelkembali.getValueAt(a, 3).toString();
        try {
            SimpleDateFormat f = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date d=f.parse(kembali);
            jkembali.setDate(d);
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_tabelkembaliMouseClicked

    private void blambatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blambatActionPerformed
        try {
            DateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            Date tanggalpinjam = format.parse(tanggal);
            Date tanggalkembali = format.parse(tanggal1);
            long tanggalpinjam1 = tanggalpinjam.getTime();
            long tanggalkembali1 = tanggalkembali.getTime();
            long diff = tanggalkembali1 - tanggalpinjam1;
            long lama = diff / (24 * 60 * 60 * 1000);
            txtlambat.setText(Long.toString(lama) + "");
        } catch (Exception e) {
            System.out.println("" + e.getMessage());
        }
    }//GEN-LAST:event_blambatActionPerformed

