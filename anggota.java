    private void tampilkan() {
        DefaultTableModel grid = new  DefaultTableModel();
        grid.addColumn("Stambuk");
        grid.addColumn("Nama Anggota");
        grid.addColumn("Tanggal Lahir");
        grid.addColumn("Jenis Kelamin");
        grid.addColumn("Alamat");
        grid.addColumn("Kelas");
        tabelanggota.setModel(grid);
        try {
            st =  koneksi.createStatement();
            Res = st.executeQuery("Select * from anggota");
            while (Res.next()) {
                grid.addRow(new Object[]{
                    Res.getString("stambuk"),
                    Res.getString("namaAnggota"),
                    Res.getString("tanggalLahir"),
                    Res.getString("jenisKelamin"),
                    Res.getString("alamat"),
                    Res.getString("kelas"),
                });
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Gagal Tampil "+e);
        }
    }

