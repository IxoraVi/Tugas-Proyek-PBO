    private void tabelanggotaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelanggotaMouseClicked
        int a = tabelanggota.getSelectedRow();
        if (a == -1){
            return;
        }
        String stambuk = (String) tabelanggota.getValueAt(a, 0);
        txtstambuk.setText(stambuk);
        String nama = (String) tabelanggota.getValueAt(a, 1);
        txtnama.setText(nama);
        String ttl = (String) tabelanggota.getValueAt(a, 2);
        txtttl.setText(ttl);
        String jeniskelamin = (String) tabelanggota.getValueAt(a, 3);
        cbtjnskelamin.setSelectedItem(jeniskelamin);
        String alamat = (String) tabelanggota.getValueAt(a, 4);
        txtalamat.setText(alamat);
        String kelas = (String) tabelanggota.getValueAt(a, 5);
        txtkelas.setText(kelas);
    }//GEN-LAST:event_tabelanggotaMouseClicked

    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String stambuk = txtstambuk.getText();
        String nama = txtnama.getText();
        String ttl = txtttl.getText();
        String alamat = txtalamat.getText();
        String kelas = txtkelas.getText();
        String jeniskelamin = (String) cbtjnskelamin.getSelectedItem();
        try {
            st = koneksi.createStatement();
            st.executeUpdate("insert into anggota VALUES ('"+ stambuk +"','"
                    + nama +"','"
                    + ttl +"','"
                    + jeniskelamin +"','"
                    + alamat +"','"
                    + kelas +"');");
            tampilkan();
            JOptionPane.showMessageDialog(null, "Data Behasil Disimpan");
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
        }
    }//GEN-LAST:event_bsimpanActionPerformed

    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        String stambuk = txtstambuk.getText();
        String nama = txtnama.getText();
        String ttl = txtttl.getText();
        String alamat = txtalamat.getText();
        String kelas = txtkelas.getText();
        String jeniskelamin = (String) cbtjnskelamin.getSelectedItem();
        try {
            st = koneksi.createStatement();
                st.executeUpdate("update anggota SET stambuk='"+stambuk+"',"
                        +"namaAnggota='"+nama+"',"
                        +"tanggalLahir='"+ttl+"',"
                        +"jenisKelamin='"+jeniskelamin+"',"
                        +"alamat='"+alamat+"',"
                        +"kelas='"+kelas+"'" 
                        + "WHERE stambuk='"+stambuk+"' OR namaAnggota='"+nama+"'");
            tampilkan();
            JOptionPane.showMessageDialog(null, "Teredit");
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Tidak Teredit\n"+e.getMessage());
        }
    }//GEN-LAST:event_beditActionPerformed

